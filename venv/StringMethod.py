#capitalize - mengubah huruf pertama menjadi kapital
kata = "kita adalah python code writer"
print(kata.capitalize())

#tittle() - mengubah awal setiap kata dalam kalimat mejadi huruf besar
kata = "kita adalah python code writer"
print(kata.title())

#upper() - mengubah semua huruf pada string menjadi huruf besar
#lower() - mengubah semua huruf pada string menjadi huruf kecil
nama = 'Dicoding'
print(nama.upper())
print(nama.lower())

#casefold() - mengubah semua karakter menjadi kecil sama kayak lower() tapi lebih kuat lebih agresiv
kata = 'DiCOding VvUX fOr U'
print(kata.casefold())

#swapcase() - mengubah huruf kecil menjadi besar , huruf besar menjadi kecil
kata = 'pAdLI deV'
print(kata.swapcase())